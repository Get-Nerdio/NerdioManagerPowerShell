Function New-NmePortalNotification {
	<#

	.SYNOPSIS

	Create portal notification.

	.DESCRIPTION

	Create portal notification. 

	This function calls the /api/v1/portal-notification endpoint of the NME REST API, using the post method.


	.PARAMETER NmeCreatePortalNotificationRestModel

	Requires an NmeCreatePortalNotificationRestModel object, as generated by the New-NmeCreatePortalNotificationRestModel command.

	#>

	[CmdletBinding()]
	Param(
		[Parameter(Mandatory=$true, ValueFromPipelineByPropertyName=$True)][ValidateScript({if ($_.PSObject.TypeNames -contains "NmeCreatePortalNotificationRestModel"){$true} else{throw " is not a NmeCreatePortalNotificationRestModel object."}})]$NmeCreatePortalNotificationRestModel
	)
	Set-NmeAuthHeaders
	Try {
		$json = $NmeCreatePortalNotificationRestModel | ConvertTo-Json -Depth 20
		Write-Debug 'json:'
		Write-Debug $json
		$Result = Invoke-RestMethod "$script:NmeUri/api/v1/portal-notification$QueryString" -Method post -Headers $script:AuthHeaders -ContentType 'application/json' -body $json
		Write-Verbose ($result | out-string)
		$Result.PSObject.TypeNames.Insert(0, 'NmePortalNotificationRestModel')
		$Result | CapProps
	}
	Catch {
		$message = ParseErrorForResponseBody($_)
		write-error ($message | out-string)
	}
}
