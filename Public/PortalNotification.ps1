	#>

	[CmdletBinding()]
	Param(

	)
	Set-NmeAuthHeaders
	Try {
		$Result = Invoke-RestMethod "$script:NmeUri/api/v1/portal-notification$Querystring" -Method get -Headers $script:AuthHeaders -ContentType 'application/json'
